
    var menuBtn = document.getElementById("menuBtn");
    var sideNav = document.getElementById("sideNav");

    sideNav.style.display = "none";
    menuBtn.onclick = function() {
        if (sideNav.style.display = "none") {
            sideNav.style.display = "block";
            
        }
        
        else {
            sideNav.style.display = "none";
        }
    }
    
    
    
